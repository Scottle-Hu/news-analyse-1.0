package com.charles.na.model;

/**
 * @author Cahrles
 * @create 2018/3/29
 * @description 同义词
 * @since 1.0
 */
public class Synonym {

    private String id;

    private String code;

    private String word;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
